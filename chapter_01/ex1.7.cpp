/******************************************************************************
(Approximate pi) Write a program that displays the result of:
4 * (1 - (1/3) + (1/5) - (1/7) + (1/9) - (1/11)) AND
4 * (1 - (1/3) + (1/5) - (1/7) + (1/9) - (1/11) + (1/13))
Use 1.0 instead 1 in the program
******************************************************************************/

#include <iostream>
using namespace std;

int main(){
    float a = 4 * (1.0 - (1.0/3) + (1.0/5) - (1.0/7) + (1.0/9) - (1.0/11));
    float b = 4 * (1.0 - (1.0/3) + (1.0/5) - (1.0/7) + (1.0/9) - (1.0/11) + (1.0/13));
    
    cout << a << endl;
    cout << b << endl;

    return 0;
}